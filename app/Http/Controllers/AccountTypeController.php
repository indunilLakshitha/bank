     */
    public function store(Request $request)
    {
        

        $acc_typ=$request;
        $acc_typ['is_enable']=1;
        $acc_typ['created_by']=Auth::user()->id;
        $acc_typ['updated_by']=Auth::user()->id;
        AccountType::create($acc_typ->all());
        return redirect()->route('accountType.index')
            ->with('success', 'Account Type Created Successfully.');
    }

