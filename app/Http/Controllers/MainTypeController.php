     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $main_typ=$request;
        $main_typ['is_enable']=1;
        $main_typ['created_by']=Auth::user()->id;
        $main_typ['updated_by']=Auth::user()->id;
        MainType::create($main_typ->all());
        return redirect()->route('mainType.index')
            ->with('success', 'Main Type Created Successfully.');
    }

