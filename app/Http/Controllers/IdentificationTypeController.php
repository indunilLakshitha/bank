     */
    public function store(Request $request)
    {
        $id_typ=$request;
        $id_typ['is_enable']=1;
        $id_typ['created_by']=Auth::user()->id;
        $id_typ['updated_by']=Auth::user()->id;
        IedentificationType::create($id_typ->all());
